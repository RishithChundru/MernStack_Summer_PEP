var str="Rishith"
var s="sai"
// length function
console.log(str.length)


// charAt function
console.log(str.charAt(3))

// charCodeAt function
console.log(str.charCodeAt('R'))

// substring function
console.log(str.substring(0,3))

// Uppercase function
console.log(str.toUpperCase())

// Lowercase function
console.log(str.toLowerCase())

// concat function
console.log(str.concat(" ",s))

// trim function
console.log(str.trim())

// replace function
console.log(str.replace('R','H'))

// split function
console.log(str.split())

